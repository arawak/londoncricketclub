                <h2 id="sec2">Membership</h2>
<p>Membership is open to anyone who is interested, and lives in London, Canada. We are not in England. We do not hire professional players.</p>

<h2>2018 Fees</h2>
<p>
The base membership fee is $50. This entitles a member to vote, hold office, and register in the league teams. Paid members also are eligible to play in any club matches or friendly matches that are arranged.
</p>
<h3>HDCL League Fee</h3>
<p>
To play in the 40 over league teams, there is a fee to cover the costs of umpires, driving, food, and related expenses. The league fee is $220 and is over and above the base membership fee. This fee is discounted to $200 for students. New members will also require a uniform, which costs $42+HST.
</p>
<hr />
<p>
Base membership fee is due upon joining for new members. Payment can be made by cheque, email transfer, or cash. Payment plans may be arranged with the treasurer</p>
<p>
Please note that there will be no proration of membership dues for past and current members. Any outstanding membership dues and other fees owing from previous seasons must be paid before 2018 membership dues.
</p>

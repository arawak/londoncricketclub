                <h2 id="sec0">Over 150 years of cricket in London Ontario</h2>
<p>London Cricket <a href="https://www.londonpubliclibrary.ca/blog/jiminy-cricket">dates back to at least 1856</a> when it was listed in the city's first directory. Over the years the club has had many homes, including Tecumseh Park, Fanshawe Park, the London Psychiatric Hospital grounds, Maitland Park, and most recently the North London Athletic Fields.</p>
<p>We are members of the <a href="https://cricclubs.com/HDCL/">Hamilton &amp; District Cricket League</a> and in 2019 will be fielding teams the Premier and First divisions.</p>
<hr />
<h3>Join Us</h3>
<p>
We play white ball 40 over matches in the Hamilton and District Cricket League, which operates under the ICC umbrella. Our members have represented HDCL, played for Ontario, and are eligible to play at the national level.
</p>
<p>
Spaces are still available for regular and casual members of all skill levels. All equipment and transportation to away games is provided. <a href="https://forms.gle/6MLkvbSGMK1nNC2G6">Click here to sign up</a>, and get a free practice session before paying your membership fees.
</p>
<p>Registration is only open to local players (living in southwestern Ontario); we do not recruit from overseas. Join by completing <a href="https://goo.gl/forms/1lsYeD8E4IA2wQVL2">the registration form</a></p>

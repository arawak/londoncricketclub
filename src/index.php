<h3>Club Activities</h3>
<p>
We field three teams in the Hamilton &amp; District Cricket League, one each division. The 40 over (white ball) league runs throughout the summer, starting in mid June, with playoffs in September. 
</p>
<p>
Monday and Thursday nights are practices for the <u>league team players only</u>.
</p>
<p>
We also play a small number of club matches and friendlies.
</p>
<p>
Indoor training sessions start in January. These are held weekly at <a href="https://www.google.ca/maps/place/Centrefield+Sports+Inc,+25+Midpark+Crescent,+London,+ON+N6N+1A9">Centrefield Sports</a> near the 401. We will transition to outdoors as weather permits. $10 per session for paid members, $15 for non-members.
</p>

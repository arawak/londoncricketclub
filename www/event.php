<h2>London Cricket Club Regulations</h2>
<p><i>reviewed April 18 2015</i></p>
<p>These regulations will be reviewed, updated as needed, and approved every year at the spring general meeting.</p>
<h2 id="officers">Officers</h2>
<p>The following club officers will be elected at the Spring General Meeting:</p>
<ol style="list-style-type: decimal">
<li>First Captain</li>
<li>Second Captain</li>
<li>Third Captain</li>
<li>Fourth Captain</li>
</ol>
<p>The following officers shall be appointed by the executive committee:</p>
<ul>
<li>Statistician</li>
<li>Assistant Club Manager</li>
</ul>
<p>The Executive Committee shall consist of the President, Vice President, Treasurer, Secretary, and Club Manager.</p>
<h2 id="expenses">Expenses</h2>
<p>For away games the club will try to organise drivers, and credit designated drivers on this schedule:</p>
<ul>
 <li>GTA/Windsor: $65</li>
 <li>Sarnia/Cambridge/Waterloo: $45</li>
</ul>
<p>Members choosing to drive on their own will not be credited.</p>
<p>Any other expenses paid must be approved by the executive.</p>
<h2 id="membership">Membership</h2>
<p>Membership dues are not refundable, except at the discretion of the Executive Committee in special circumstances.</p>
<p>Every member should expect to prepare tea at least once during the season, and will be credited $50 for this.</p>
<p>Unless specified otherwise, there is a $5 match fee for all games.</p>
<p>Non-members will be expected to pay $10 per game, in addition to the match fee (total $15).</p>
<p>At the beginning of June, July, and August all members will ensure they have at least $20 credit, above and beyond their membership dues. Any credit unused will be refunded at the end of the season. If a member has more than $20 credit at the beginning of the month (for example, from driving fees), he may request a refund for the amount in excess of $20.</p>
<p>Members are requested to inform the selectors of their availability and/or non-availability every week. If possible, members should make their availabilities known in advance.</p>
<p>For UFCL league matches, the coloured team uniform must be worn. For all other matches, cricket whites are required.</p>
<p>Pavilion members may play up to five games and are not eligible for UFCL matches. After this, they are treated as non-members for the purposes of selection and game fees. Pavilion members may upgrade their membership at any time.</p>
<p>For friendly matches members may expect to be chosen, by the end of the season, at least 50% of the number of times they make themselves available on time.</p>
<p>Members who are dissatisfied with the operation of the club, or who are having personal conflicts with another member in the club, should take the matter to the vice president.</p>
<h1 id="selection-policy">Selection Policy</h1>
<p>The selection committee may vary from week to week, but it will consist of one executive member, one elected captain, and one other member of the club. The vice president shall maintain the selection committee schedule.</p>
<p>Players are expected to make themselves available before Tuesday at 6pm. The selection committee shall pick the best team it can from these players, balancing where possible with giving all members fair access to games. Factors that may influence selection include:</p>
<ul>
<li>performance in previous games</li>
<li>effort put in during games and practices</li>
<li>positive attitude towards teammates</li>
<li>willingness to put in fair share of game day duties (scoring, umpiring, ground preparation)</li>
<li>volunteering for 12th man duties, scoring, and umpiring</li>
</ul>
<p>Sometimes the ability to be a driver may need to be a factor in selection for away games.</p>
<p>Normally the captain and vice captain will be, respectively, the two most senior captains selected to the team. When necessary, the selection committee will appoint the captain and/or vice captain from the selected team.</p>
<h1 id="code-of-conduct">Code of Conduct</h1>
<p>Members are expected to conduct themselves in a manner that will enhance the reputation of London Cricket Club and of Cricket in general, both on and off the field.</p>
<p>Players will show respect to each other, to the other team, to umpires, and to spectators. Players must at all times, without dissent, accept the decision of the umpires.</p>
<p>Once selected, all players are expected to honour the selection. When unavoidable circumstances arise that prevent a player from playing after being selected, players are expected to notify the selection committee immediately.</p>
<p>Players should arrive at the ground at least 20 minutes before the scheduled start of a match and be dressed to commence play on time. Appropriate kit and dress should be worn during games.</p>
<p>For home games and practices all players are required to prepare the field prior to and clean up at the conclusion of a game i.e. the placing of mat, stumps, boundary markers, scoreboards and numbers, crease markings, etc, making sure that the team equipment is properly used and stored.</p>
<p>Disciplinary issues will be resolved by the disciplinary committee. Disciplinary action may include fines or suspensions.</p>
<p>Members are asked to ensure that the Club’s facilities are not abused, and that nothing is left behind after club events, including equipment, clothing, and trash.</p>
<p>All members and anyone playing for the Club are bound by the Club Disciplinary Rules and shall conduct themselves accordingly.</p>
<p>Unsatisfactory conduct shall include, but not be limited to:</p>
<ul>
<li><p>Failure to respect the captain's authority during a match.</p></li>
<li><p>Dissent at an umpire’s decision or reaction in a provocative or disapproving manner, whether verbal or written.</p></li>
<li><p>Physical abuse, intimidation, assault or attempt to intimidate or assault an umpire, official, player or spectator.</p></li>
<li><p>The use of crude, foul or abusive language or the making of offensive gestures or hand signals whether directed at an individual or otherwise.</p></li>
<li><p>Sledging or deliberate distraction of an opponent and persisting appealing in order to pressurise an umpire or opponent.</p></li>
<li><p>Any form of abuse or discrimination relating to race, religion, creed or any other nature.</p></li>
<li><p>Wilful damage to property, equipment or playing surface.</p></li>
<li><p>The use or distribution of illegal drugs or substances.</p></li>
<li><p>Conduct unbecoming or any other action that is likely to bring the Club or Game of Cricket into disrepute or prejudice its good name or interests.</p></li>
<li><p>Repeated infringements of the Spirit of the Game, either by an individual or a team where each infringement in itself does not merit any immediate disciplinary action. Such instances will result in a Disciplinary Hearing.</p></li>
<li><p>Unacceptable behaviour, by word or action, at any function organised by the Club.</p></li>
<li><p>Mistreatment or harassment of an individual or group either in person or via electronic equipment including, but not limited to, mobile phones, email and social networking sites.</p></li>
</ul>
